# For Loop with lists
l = [1, 4, 6, 234, 6, 764] # list

for i in l: # i is a loop variable
    print(i)


# For Loop with tuples
t = (6, 231, 75, 122) # tuple

for i in t: # i is a loop variable
    print(i)


# For Loop with strings
s = "Jude" # string

for i in s: # i is a loop variable
    print(i)